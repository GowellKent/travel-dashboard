<template>
    <div class="container mt-5">
        <div class="row">
            <div class="col-md-12">
                <div class="card border-0 rounded shadow">
                    <div class="card-body">
                        <div class="row">
                            <div class="col-11">
                                <h4>EDIT DATA BUS</h4>
                            </div>
                            <div class="col right-align">
                                <router-link :to="{ name: 'bus.index' }" class="btn btn-md btn-primary">BACK</router-link>
                            </div>
                        </div>
                        <hr>

                        <form @submit.prevent="update">
                            <div class="form-group">
                                <label for="title" class="font-weight-bold mt-2 mb-1">Nama Bus</label>
                                <input type="text" class="form-control" v-model="bus.tb_nama"
                                    placeholder="Masukkan Nama Bus">
                            </div>
                            <br>
                            <div class="form-group">
                                <label for="provinsi" class="font-weight-bold">Provinsi Asal</label>
                                <!-- <input class="form-control" v-model="kota.tot_provinsi" placeholder="Masukkan Provinsi"> -->
                                <select class="form-select" v-model="bus.tph_provinsi_asal"
                                    v-on:change="getKota(bus.tph_provinsi_asal)" aria-label="Provinsi Asal">
                                    <!-- <option class="dropdown-item">Provinsi</option> -->
                                    <option v-for="data in provs" :key="data.id">{{ data.id }}-{{ data.nama }}</option>
                                </select>
                            </div>
                            <div class="form-group">
                                <label for="nama" class="font-weight-bold">Kota Asal</label>
                                <br />
                                <div class="row">
                                    <div class="col">
                                        <input type="text" class="form-control" v-model="bus.tb_kota_asal"
                                            placeholder="Masukkan Kota" disabled>
                                    </div>
                                    <div class="col">
                                        <select class="form-select" v-model="bus.tb_kota_asal" aria-label="Kota Asal">
                                            <option v-for="data in kotas" :key="data.id">{{ data.nama }}</option>
                                        </select>
                                    </div>
                                </div>
                            </div>
                            <br>
                            <div class="form-group">
                                <label for="provinsi" class="font-weight-bold">Provinsi Tujuan</label>
                                <!-- <input class="form-control" v-model="kota.tot_provinsi" placeholder="Masukkan Provinsi"> -->
                                <select class="form-select" v-model="bus.tph_provinsi"
                                    v-on:change="getKota2(bus.tph_provinsi)" aria-label="Provinsi Tujuan">
                                    <!-- <option class="dropdown-item">Provinsi</option> -->
                                    <option v-for="data in provs2" :key="data.id">{{ data.id }}-{{ data.nama }}</option>
                                </select>
                            </div>
                            <div class="form-group">
                                <label for="nama" class="font-weight-bold">Kota Tujuan</label>
                                <br />
                                <div class="row">
                                    <div class="col">
                                        <input type="text" class="form-control" v-model="bus.tb_kota_tujuan"
                                            placeholder="Masukkan Kota" disabled>
                                    </div>
                                    <div class="col">
                                        <select class="form-select" v-model="bus.tb_kota_tujuan" aria-label="Kota Asal">
                                            <option v-for="data in kotas2" :key="data.id">{{ data.nama }}</option>
                                        </select>
                                    </div>
                                </div>
                            </div>
                            <div class="form-group">
                                <label for="title" class="font-weight-bold mt-2 mb-1">Pax</label>
                                <input type="text" class="form-control" v-model="bus.tb_pax" placeholder="Masukkan Pax Bus">
                            </div>
                            <br /><br />
                            <button type="submit" class="btn btn-primary">SIMPAN</button>
                        </form>

                    </div>
                </div>
            </div>
        </div>
    </div>
</template>

<script>

import { reactive, ref, onMounted } from 'vue'
import { useRouter, useRoute } from 'vue-router'
import axios from 'axios'
import { baseURL } from '@/config.js'

export default {

    data() {
        return {
            jenis: [],
            provs: [],
            provs2: [],
            kotas: [],
            kotas2: [],
        }
    },
    methods: {
        setProvs(data) {
            this.provs = data;
        },
        setProvs2(data) {
            this.provs2 = data;
        },

        setKotas(data) {
            this.kotas = data;
        },
        setKotas2(data) {
            this.kotas2 = data;
        },

        getKota(id) {
            let idProvinsi = id.split("-")[0]
            axios.get('https://dev.farizdotid.com/api/daerahindonesia/kota?id_provinsi=' + idProvinsi)
                .then(ress => {
                    this.setKotas(ress.data.kota_kabupaten)
                })
                .catch(error => {
                    console.log(error)
                })

        },
        getKota2(id) {
            let idProvinsi = id.split("-")[0]
            axios.get('https://dev.farizdotid.com/api/daerahindonesia/kota?id_provinsi=' + idProvinsi)
                .then(ress => {
                    this.setKotas2(ress.data.kota_kabupaten)
                })
                .catch(error => {
                    console.log(error)
                })

        }
    },
    mounted() {
        axios.get('https://dev.farizdotid.com/api/daerahindonesia/provinsi')
            .then(ress => {
                this.setProvs(ress.data.provinsi)
                this.setProvs2(ress.data.provinsi)
            })
            .catch(error => {
                console.log(error)
            })
    },

    setup() {

        //state posts
        const bus = reactive({
            tb_nama: '',
            tb_kota_asal: '',
            tb_kota_tujuan: '',
            tb_pax: ''
        })

        //state validation
        const validation = ref([])

        //vue router
        const router = useRouter()

        //vue router
        const route = useRoute()

        //mounted
        onMounted(() => {

            //get API from Laravel Backend
            axios.get(baseURL + `/bus/find?tb_kode=${route.params.id}`)
                .then(response => {

                    //assign state posts with response data
                    bus.tb_nama = response.data[0].tb_nama
                    bus.tb_kota_asal = response.data[0].tb_kota_asal
                    bus.tb_kota_tujuan = response.data[0].tb_kota_tujuan
                    bus.tb_pax = response.data[0].tb_pax

                }).catch(error => {
                    console.log(error.response.data)
                })

        })

        //method update
        function update() {

            let tb_nama = bus.tb_nama
            let tb_kota_asal = bus.tb_kota_asal
            let tb_kota_tujuan = bus.tb_kota_tujuan
            let tb_pax = bus.tb_pax

            axios.get(baseURL+'/bus/update', {
                params: {
                    tb_kode: route.params.id,
                    tb_nama: tb_nama,
                    tb_kota_asal: tb_kota_asal,
                    tb_kota_tujuan: tb_kota_tujuan,
                    tb_pax: tb_pax
                }
            }).then(() => {

                //redirect ke post index
                router.push({
                    name: 'bus.index'
                })

            }).catch(error => {
                //assign state validation with error 
                // validation.value = error.response.data
                console.log(error)
            })

        }

        //return
        return {
            bus,
            validation,
            router,
            update
        }

    }

}
</script>

